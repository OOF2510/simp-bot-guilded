const { Client, Message } = require("guilded.ts");

module.exports = {
  /**
   * Executes the command
   * @param {Message} msg
   * @param {Client} client
   * @param {*} config
   */
  async execute(msg, client, config) {
    msg.reply(`Simp Bot is in ${client.guilds.cache.size} servers!`);
  },
};
